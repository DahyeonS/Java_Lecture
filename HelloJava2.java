
public class HelloJava2 {
	public static void main(String[] args) {
//		자동 완성: Ctrl + Space
//		프로그램 실행: Ctrl + F11

//		주석: 프로그램에 대한 간단한 설명 (코드 설명)
//		프로그램 실행 시 실행되지 않는다.
//		1줄 주석: 코드 앞에 "/"를 연속해서 2개 사용
//		=> 단축키: Ctrl + /
//		여러줄 주석: "/* ~ */" 사이에 입력
//		=> 단축키: Ctrl + Shift + /
//		=> 단축키 해제: Ctrl + Shift + \

//		코드 복사: Ctrl + Alt + 위/아래 방향키
//		코드 이동: Alt + 위/아래 방향키
//		한 줄 삭제: Ctrl + d
//		자동 들여쓰기: Ctrl + i
//		코드 자동 정렬: Ctrl + Shift + f
		System.out.println("Hello~ Java!!");
		System.out.print("안녕\n 자바!"); // \n => new line(줄바꿈)
		System.out.println("Hey~ 자바!");
		System.out.printf("Hey~");
		System.out.println("Hello~ Java!!");
		
//		println: 괄호 안의 내용을 출력하고 줄을 바꾼다.
//		print: 괄호 안의 내용을 출력하고 줄을 바꾸지 않는다.
//		printf: 출력서식(자리수)을 지정해서 출력한다.
		
	}

}
